# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo.
# positivo.
print(11) # int
print(-11) # int
print(0)
#float -> Número com ponto flutuante
# O tipo float representa qualquer número
#positivo ou negativo com ponto flutuante.
#float sem sinal é considerado posistivo.
print(11.1, 1.22)
print(0.0, -1,5)
# A função type mostra o tipo que o Python
# inferiu ao valor.

print(type(0.0))

print(type("Vitor Hugo"))

print(type(1))

print(type(-1))

print(type(0))

print(type("Moura"), type(0.1), type(2*2), type(2))